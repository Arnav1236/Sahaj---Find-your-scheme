function startSearch() {
  if (!answers['occupation']) {
    document.getElementById('err5').classList.add('show');
    return;
  }
  
  closeModal();
  switchScreen('screen-loading'); 
  
  const payload = { ...answers, language: currentLang };
  
  fetch('http://localhost:5000/match', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(payload) 
  })
  .then(res => {
      if (!res.ok) throw new Error(`HTTP error! Status: ${res.status}`);
      return res.json();
  })
  .then(data => {
      console.log("Raw Data received from backend:", data);
      
      const formattedSchemes = (data.schemes || []).map(s => {
          const lang = currentLang; 
          return {
              ...s, 
              name: s[`name_${lang}`] || s.name_en || s.name,
              ministry: s[`ministry_${lang}`] || s.ministry_en || s.ministry,
              desc: s[`desc_${lang}`] || s.desc_en || s.desc,
              benefit: s[`benefit_${lang}`] || s.benefit_en || s.benefit,
              eligibility_summary: s[`eligibility_summary_${lang}`] || s.eligibility_summary_en || s.eligibility_summary,
              documents: s[`documents_${lang}`] || s.documents_en || s.documents,
              mode: s[`mode_${lang}`] || s.mode_en || s.mode,
              beneficiary: s[`beneficiary_${lang}`] || s.beneficiary_en || s.beneficiary,
              states: s[`states_${lang}`] || s.states_en || s.states
          };
      });

      setTimeout(() => processResults(formattedSchemes), 1500);
  })
  .catch(err => {
      console.error('Backend connection failed:', err);
      alert("Could not connect to the database. Ensure your Flask server is running on port 5000.");
      switchScreen('screen-welcome');
  });
}

