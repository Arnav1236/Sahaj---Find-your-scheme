function processResults(matched) {
    fetchedSchemes = matched;
    activeCategory = 'All';
    activeState = 'All';
    
    // Send to Firebase DB History if logged in
    window.userAnswers = answers;
    if (window.saveHistory && fetchedSchemes.length > 0) {
        window.saveHistory(fetchedSchemes);
    }

    renderStateDropdown();
    renderCategoryBar();
    renderSchemesGrid();
    
    document.getElementById('pin-input').value = '';
    document.getElementById('kendra-results').innerHTML = '';
    switchScreen('screen-results');
}

function renderStateDropdown() {
    const select = document.getElementById('state-filter');
    const t = translations[currentLang];
    
    if (fetchedSchemes.length === 0) { 
        select.style.display = 'none'; 
        return; 
    }
    select.style.display = 'inline-block';

    const uniqueStates = [...new Set(fetchedSchemes.map(s => s.states_en || s.states).filter(Boolean))];
    
    let optionsHtml = `<option value="All">${t.state_all}</option>`;
    uniqueStates.forEach(state => {
        let displayState = state;
        if (stateKeyMap[state] && t[stateKeyMap[state]]) {
            displayState = t[stateKeyMap[state]];
        }
        optionsHtml += `<option value="${state}" ${state === activeState ? 'selected' : ''}>${displayState}</option>`;
    });
    
    select.innerHTML = optionsHtml;
}

function filterState(selectedState) {
    activeState = selectedState;
    renderSchemesGrid();
}

function renderCategoryBar() {
    const bar = document.getElementById('category-bar');
    if (fetchedSchemes.length === 0) { bar.innerHTML = ''; return; }

    const t = translations[currentLang];
    const categories = ['All', ...new Set(fetchedSchemes.map(s => s.category).filter(Boolean))];

    bar.innerHTML = categories.map(cat => {
        let displayCat = cat;
        if (cat === 'All') displayCat = t.all_cat;
        else if (categoryKeyMap[cat] && t[categoryKeyMap[cat]]) { displayCat = t[categoryKeyMap[cat]]; }

        const activeClass = cat === activeCategory ? 'active' : '';
        return `<button class="cat-pill ${activeClass}" onclick="filterCategory('${cat}')">${displayCat}</button>`;
    }).join('');
}

function filterCategory(cat) {
    activeCategory = cat;
    renderCategoryBar();
    renderSchemesGrid();
}

function renderSchemesGrid() {
    const grid = document.getElementById('schemes-grid');
    const t = translations[currentLang];

    let schemesToShow = fetchedSchemes.filter(s => {
        const matchCat = (activeCategory === 'All' || s.category === activeCategory);
        const stateRaw = s.states_en || s.states;
        const matchState = (activeState === 'All' || stateRaw === activeState || stateRaw === "All India");
        return matchCat && matchState;
    });

    if (schemesToShow.length === 0) {
        grid.innerHTML = `<div style="text-align:center; padding: 60px; background: white; border-radius: 20px;"><h3 style="color: var(--blue);">No exact matches found</h3></div>`;
    } else {
        grid.innerHTML = schemesToShow.map((s) => {
            const actualIndex = fetchedSchemes.indexOf(s);
            const cardIcon = s.icon || `<svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="var(--blue)" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M12 22s8-4 8-10V5l-8-3-8 3v7c0 6 8 10 8 10z"></path></svg>`;

            return `
      <div class="scheme-card" onclick="openSchemeModal(${actualIndex})">
        <div class="scheme-card-header">
            <div class="scheme-icon-wrapper">${cardIcon}</div>
            <div>
                <h3>${s.name}</h3>
                <div class="scheme-ministry">${s.ministry || ''}</div>
            </div>
        </div>
        <div class="scheme-badges">
            ${s.benefit ? `<span class="badge-benefit">${s.benefit}</span>` : ''}
            ${s.mode ? `<span class="badge-mode">${s.mode}</span>` : ''}
        </div>
        <p>${s.desc || ''}</p>
        <div class="view-more">${t.view_details}</div>
      </div>
    `}).join('');
    }
}

