function searchKendra() {
    const pin = document.getElementById('pin-input').value;
    const resDiv = document.getElementById('kendra-results');
    
    if (pin.length !== 6 || isNaN(pin)) {
        resDiv.innerHTML = `<p style="color:red; font-size:0.95rem; font-weight:600;">⚠️ Please enter a valid 6-digit PIN code.</p>`;
        return;
    }

    resDiv.innerHTML = `
        <div style="display:flex; justify-content:center; padding: 20px;">
            <svg class="spinner" width="30" height="30" viewBox="0 0 24 24" fill="none" stroke="var(--blue)" stroke-width="3" stroke-dasharray="15 15" stroke-linecap="round"><circle cx="12" cy="12" r="10"></circle></svg>
        </div>
    `;

    fetch(`https://api.postalpincode.in/pincode/${pin}`)
    .then(response => response.json())
    .then(data => {
        if (data[0].Status === "Success") {
            const postOffices = data[0].PostOffice;
            const topOffices = postOffices.slice(0, 3);
            resDiv.innerHTML = topOffices.map(po => `
                <div class="kendra-card">
                    <h4>🏢 ${po.Name} Post Office / Sahaj Kendra</h4>
                    <p>${po.Block ? po.Block + ', ' : ''}${po.District}, ${po.State}</p>
                    <div class="kendra-dist">📍 Service Type: ${po.BranchType}</div>
                </div>
            `).join('');
        } else {
            resDiv.innerHTML = `<p style="color:red; font-size:0.95rem; font-weight:600;">⚠️ No official centers found for this PIN code.</p>`;
        }
    })
    .catch(err => {
        console.error("Error fetching PIN data:", err);
        resDiv.innerHTML = `<p style="color:red; font-size:0.95rem; font-weight:600;">⚠️ Error connecting to server.</p>`;
    });
}

